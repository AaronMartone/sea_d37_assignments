/*
    Semantic Versioning (SemVer)

    <MAJOR>.<MINOR>.<PATCH>, ie: 1.12.3

    MAJOR version. This number is incremented when changes are made that are incompatible with previous versions.
    MINOR version. This number is incremented when functionality is added in a backwards-compatible manner.
    PATCH version. This number is incremented when you make backwards-compatible bug fixes.

    "Dependency Hell" is a term used that indicates that as a project's size grows, so too does its dependencies on
    other librarys and files. This can quickly become unwiedly and difficult to manage, as files are dependent on files
    which are dependent on others, etc.

    If dependencies are too loose or tight, this can prevent your project from progressing, as it is relying on other
    files which may become incompatible for either reason as you continue development.
*/